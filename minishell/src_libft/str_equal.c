#include "libft.h"

_Bool	str_equal(char *s1, char *s2)
{
	while ((s1 && s2) && (*s1 || *s2))
	{
		if (*s1 != *s2)
			return (0);
		s1++;
		s2++;
	}
	return (1);
}
